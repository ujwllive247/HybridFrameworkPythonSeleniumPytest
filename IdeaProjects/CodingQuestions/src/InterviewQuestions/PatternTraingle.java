package InterviewQuestions;// Using Nested Loops

public class PatternTraingle {
    public static void main(String[] args) {
        int n = 25;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }}}






